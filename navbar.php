<script>
    tailwind.config = { darkMode: 'class' }
    const themeToggleDarkIcon = document.getElementById('theme-toggle-dark-icon');
    const themeToggleLightIcon = document.getElementById('theme-toggle-light-icon');

    if (localStorage.getItem('theme') === 'dark' || (!('theme' in localStorage) && window.matchMedia('(prefers-color-scheme: dark)').matches)) {
        document.documentElement.classList.add('dark');
        themeToggleLightIcon.classList.remove('hidden');
    } else {
        document.documentElement.classList.remove('dark');
        themeToggleDarkIcon.classList.remove('hidden');
    }

    const themeToggleBtn = document.getElementById('theme-toggle');
    themeToggleBtn.addEventListener('click', function() {
        themeToggleDarkIcon.classList.toggle('hidden');
        themeToggleLightIcon.classList.toggle('hidden');

        if (document.documentElement.classList.contains('dark')) {
            document.documentElement.classList.remove('dark');
            localStorage.setItem('theme', 'light');
        } else {
            document.documentElement.classList.add('dark');
            localStorage.setItem('theme', 'dark');
        }
    });

    // --- PRESERVE SCROLL POSITION ACROSS RELOADS / SUBMISSIONS ---
    window.addEventListener('DOMContentLoaded', () => {
        const scrollKey = 'scroll_pos_' + window.location.pathname;
        const savedScrollPos = localStorage.getItem(scrollKey);
        if (savedScrollPos) {
            window.scrollTo(0, parseInt(savedScrollPos, 10));
            // Ensure any delayed layout or dynamic height adjustments don't disrupt the scroll
            setTimeout(() => {
                window.scrollTo(0, parseInt(savedScrollPos, 10));
                localStorage.removeItem(scrollKey);
            }, 50);
            setTimeout(() => {
                window.scrollTo(0, parseInt(savedScrollPos, 10));
            }, 150);
        }
    });

    // Save scroll position just as the page is about to unload
    window.addEventListener('beforeunload', () => {
        const scrollKey = 'scroll_pos_' + window.location.pathname;
        localStorage.setItem(scrollKey, window.scrollY);
    });
</script>